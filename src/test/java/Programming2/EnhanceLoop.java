package Programming2;

public class EnhanceLoop {
	public static void main(String[] args) {
		int num[] = {1,2,3,4};
		for(int n:num) {
			System.out.println(n);
		}
		System.out.println();
		String Strings[] = {"Hi", "Hello", "Good Morning"};
		for(String sts:Strings) {
			System.out.println(sts);
		}
	}
}